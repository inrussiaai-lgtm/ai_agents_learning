# 1. Сбор данных
price_per_million = float(input("Введите цену за 1 млн токенов ($): "))
token_used = int(input("Сколько токенов потрачено: "))

# 2. Логика расчета
# Считаем стоимость одного токена и умножаем на количество
cost_usd = (price_per_million / 1_000_000) * token_used
cost_rub = cost_usd * 90  # 1 доллар = 90 рублей

# 3. Вывод результата
print(f"--- Результат ---")
print(f"Стоимость запроса: ${cost_usd:.6f}")
print(f"Стоимость в рублях: {cost_rub:.2f} руб.")